#include<stdio.h>
void main()
{
    int a,b;
    printf("enter the two numbers");
    scanf("%d%d",&a,&b);

    /*addition arithmetic operator*/
     printf("a+b=%d\n",a+b);

    /*subtraction arithmetic operator*/
    printf("a-b=%d\n",a-b);

    /*multiplication arithmetic operator*/
    printf("a*b=%d\n",a*b);

    /*division arithmetic operator*/
    printf("a/b=%d\n",a/b);

    /*modulus arithmetic operator*/
    printf("a%b=%d\n",a%b);

    getch();

}
